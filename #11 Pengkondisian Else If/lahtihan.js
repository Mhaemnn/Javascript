var totalMobil = 10;

for (var noMobil = 1; noMobil <= totalMobil; noMobil++) {
    if (noMobil <= 6) {
        console.log('Mobil No.' + noMobil + ' beroperasi dengan baik');

    } else if (noMobil === 8) {

        console.log('Mobil No. ' + noMobil + ' sedang lembur');
    } else {

        console.log('Mobil No.' + noMobil + ' sedang tidak beroperasi');
    }
}




